# Crie um programa que leia dois números e mostre a soma entre eles

# Solicitando ao usuário os números 
numero1 = int(input("Digite o primeiro número"))
numero2 = int(input("Digite o segundo númrero"))

# Fazendo a soma dos números solicitados
soma = numero1 + numero2

# Exibindo na tela o resultado
print("A soma entre o número {} e o {}, vale {}".format(numero1, numero2, soma))

