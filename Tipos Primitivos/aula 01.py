# Pedindo para o usuário digital 
n1 = int(input('Digite um número: '))
n2 = int(input('Digite mais um número: '))

# Soma dos dois valores digitados
s = n1 + n2

# Mostrar para o usuário a soma
print('A soma vale', s)

# Print com mais recursos
print('A soma vale {}'.format(s))

# Tipos primitivos:
# int = número inteiro (ex: 7, -4, 0, 92839)
# float = número com ponto (ex: 4.5, 0.076, 0.99999)
# bool = boleanos (ex: True e False) - a primeira letra maiuscula
# str = strings (ex: 'escrita', 'ola', '8')

